void initBoard(char b[8][8])
{
    b[0][0] = 'R';
    b[0][1] = 'N';
    b[0][2] = 'B';
    b[0][3] = 'K';
    b[0][4] = 'Q';
    b[0][5] = 'B';
    b[0][6] = 'N';
    b[0][7] = 'R';
    int i, j;
    for (i = 0; i < 8; i++)
    {
        b[1][i] = 'P';
    }
    for (i = 2; i < 6; i++)
    {
        for (j = 0; j < 8; j++)
        {
            b[i][j] = ' ';
        }
    }
    for (i = 0; i < 8; i++)
    {
        b[6][i] = 'p';
    }
    b[7][0] = 'r';
    b[7][1] = 'n';
    b[7][2] = 'b';
    b[7][3] = 'k';
    b[7][4] = 'q';
    b[7][5] = 'b';
    b[7][6] = 'n';
    b[7][7] = 'r';
}